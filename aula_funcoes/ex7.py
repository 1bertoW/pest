def soma(num1 : float,num2:float):
    resul = num1 + num2
    return resul

def mult(num1 : float,num2:float):
    resul = num1 * num2
    return resul

def sub(num1 : float,num2:float):
    resul = num1 - num2
    return resul

def div(num1 : float,num2:float):
    resul = num1 / num2
    return resul

resul_soma = soma(10,6)
print(resul_soma)

resul_mult = mult(3,6)
print(resul_mult)

resul_div = div(7,6)
print(resul_div)

resul_sub = sub(3,6)
